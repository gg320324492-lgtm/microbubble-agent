_MEETING_TRANSCRIPT_PATTERNS = ("【", "转录", "会议记录", "发言内容", "发言：")


def _is_meeting_transcript_query(query: str) -> bool:
    """检测用户消息是否包含会议转录文字

    返回 True 时应注入 get_meeting_analyzer_prompt() 到 system。
    """
    if not query:
        return False
    return any(p in query for p in _MEETING_TRANSCRIPT_PATTERNS)


def get_meeting_analyzer_prompt() -> str:
    """会议分析助手专用提示词

    当检测到用户输入是会议转录时，追加到 system prompt。
    """

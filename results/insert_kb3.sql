## 五、简化的估算公式

如果要做快速估算，可以用这个简化公式：

$$T_{max} \approx T_0 \cdot \left(\frac{R_0}{R_{min}}\right)^{3(\gamma-1)}$$

其中 $R_0/R_{min}$ 是坍缩比（初始半径与最小半径之比）。

**要达到5000K需要的坍缩比**：

$$5000 = 300 \times \left(\frac{R_0}{R_{min}}\right)^{1.2}$$

$$\frac{R_0}{R_{min}} = \left(\frac{5000}{300}\right)^{1/1.2} \approx (16.67)^{0.833} \approx 10.6$$

即**体积缩小约1200倍**（$10.6^3$）即可达到5000K。


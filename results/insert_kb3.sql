## 四、5000K的物理意义

5000K这个数值在文献中被广泛引用，其来源主要是：

1. **声致发光（Sonoluminescence）实验**：超声空化气泡破裂时的光谱分析显示，等效黑体辐射温度约为5000~15000K
2. **数值模拟**：考虑真实气体效应（电离、解离）后的模拟结果
3. **自由基产生**：在此温度下，水分子可以解离产生 ·OH、·H 等活性自由基，这正是微纳米气泡在水处理中发挥作用的关键机制

